package ControlRecetas;

public class TipodeReceta {
	
	private String receta1;
	private String receta2;
	private String receta3;

	
	public TipodeReceta(String receta1, String receta2, String receta3) {
		super();
		this.receta1 = receta1;
		this.receta2 = receta2;
		this.receta3 = receta3;
		
		
	}


	public String getReceta1() {
		return receta1;
	}


	public void setReceta1(String receta1) {
		this.receta1 = receta1;
	}


	public String getReceta2() {
		return receta2;
	}


	public void setReceta2(String receta2) {
		this.receta2 = receta2;
	}


	public String getReceta3() {
		return receta3;
	}


	public void setReceta3(String receta3) {
		this.receta3 = receta3;
	}
	

}
